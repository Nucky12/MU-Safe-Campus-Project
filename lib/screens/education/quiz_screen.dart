                    ],
                  ),
                );
              },
            ),
          ),
          Padding(
            padding: const EdgeInsets.all(20),
            child: ElevatedButton(
              onPressed: () => Navigator.pop(context),
              style: ElevatedButton.styleFrom(backgroundColor: const Color(0xFF1D0A45), minimumSize: const Size(double.infinity, 50)),
              child: const Text('Back to Quizzes', style: TextStyle(color: Color(0xFFFFD700))),
            ),
          )
        ],
      ),
    );
  }
