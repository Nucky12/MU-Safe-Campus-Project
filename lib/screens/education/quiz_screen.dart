                  margin: const EdgeInsets.only(bottom: 15),
                  padding: const EdgeInsets.all(15),
                  decoration: BoxDecoration(
                    color: isCorrect ? Colors.green.withOpacity(0.1) : Colors.red.withOpacity(0.1),
                    borderRadius: BorderRadius.circular(10),
                    border: Border.all(color: isCorrect ? Colors.green : Colors.red),
                  ),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text('ข้อที่ ${index + 1}: ${q.questionText}', style: const TextStyle(fontWeight: FontWeight.bold)),
                      const SizedBox(height: 8),
                      Text('คำตอบของคุณ: ${q.userAnswerIndex != null ? q.options[q.userAnswerIndex!] : "ไม่ได้ตอบ"}', 
                           style: TextStyle(color: isCorrect ? Colors.green[700] : Colors.red[700])),
