class ActiveQuizScreen extends StatefulWidget {
  final QuizCategory category;
  final QuizSet quizSet;
  const ActiveQuizScreen({super.key, required this.category, required this.quizSet});

  @override
  State<ActiveQuizScreen> createState() => _ActiveQuizScreenState();
}

class _ActiveQuizScreenState extends State<ActiveQuizScreen> {
  int _currentIndex = 0;
  int _score = 0;
  int? _selectedIdx;

  void _handleNext() {
    if (_selectedIdx == null) return;
    
    // บันทึกคำตอบ
    widget.quizSet.questions[_currentIndex].userAnswerIndex = _selectedIdx;
    if (_selectedIdx == widget.quizSet.questions[_currentIndex].correctAnswerIndex) _score++;

    if (_currentIndex < widget.quizSet.questions.length - 1) {
      setState(() { _currentIndex++; _selectedIdx = null; });
    } else {
      _finishQuiz();
    }
  }

  // --- ฟังก์ชันเซฟคะแนนขึ้น Firebase และเก็บลงเครื่อง ---
  Future<void> _finishQuiz() async {
    final prefs = await SharedPreferences.getInstance();
    String key = "score_${widget.category.titleEn}_${widget.quizSet.title}";
    int totalQuestions = widget.quizSet.questions.length;

    // 1. เซฟลงเครื่อง (ให้แอปทำงานไว ไม่ต้องรอเน็ต)
    await prefs.setInt(key, _score);
    await prefs.setInt("${key}_total", totalQuestions);

    // 2. เซฟขึ้น Firebase ในกล่องข้อมูลของผู้ใช้คนนั้นๆ
    try {
      User? currentUser = FirebaseAuth.instance.currentUser;
      if (currentUser != null) {
        await FirebaseFirestore.instance.collection('users').doc(currentUser.uid).set({
          'quizScores': {
            key: _score,
            '${key}_total': totalQuestions,
          }
        }, SetOptions(merge: true)); // ใช้ merge: true เพื่อไม่ให้ทับข้อมูลเก่าอื่นๆ
      }
    } catch(e) {
      print("Error saving score to Firebase: $e");
    }

    if (!mounted) return;
    Navigator.pushReplacement(context, MaterialPageRoute(builder: (context) => QuizResultScreen(category: widget.category, quizSet: widget.quizSet, score: _score)));
  }

  @override
  Widget build(BuildContext context) {
    final q = widget.quizSet.questions[_currentIndex];
    return Scaffold(
      appBar: AppBar(backgroundColor: const Color(0xFF1D0A45), title: Text('Question ${_currentIndex + 1}/${widget.quizSet.questions.length}', style: const TextStyle(color: Color(0xFFFFD700)))),
      body: Column(
        children: [
          LinearProgressIndicator(value: (_currentIndex + 1) / widget.quizSet.questions.length, backgroundColor: Colors.grey[200], valueColor: AlwaysStoppedAnimation(widget.category.color)),
          Expanded(
            child: SingleChildScrollView(
              padding: const EdgeInsets.all(20),
              child: Column(
                children: [
                  Text(q.questionText, style: const TextStyle(fontSize: 22, fontWeight: FontWeight.bold), textAlign: TextAlign.center),
                  const SizedBox(height: 30),
                  ...List.generate(q.options.length, (i) => _buildOption(i, q.options[i])),
                ],
              ),
            ),
          ),
          Padding(
            padding: const EdgeInsets.all(20),
            child: SizedBox(width: double.infinity, height: 55, child: ElevatedButton(
              onPressed: _selectedIdx != null ? _handleNext : null,
              style: ElevatedButton.styleFrom(backgroundColor: const Color(0xFF1D0A45)),
              child: Text(_currentIndex == widget.quizSet.questions.length - 1 ? 'Finish' : 'Next', style: const TextStyle(color: Color(0xFFFFD700), fontSize: 18)),
            )),
          )
        ],
      ),
    );
  }

  Widget _buildOption(int i, String text) {
    bool isSelected = _selectedIdx == i;
    return GestureDetector(
      onTap: () => setState(() => _selectedIdx = i),
      child: Container(
        margin: const EdgeInsets.only(bottom: 12),
        padding: const EdgeInsets.all(16),
        decoration: BoxDecoration(
          color: isSelected ? const Color(0xFF1D0A45) : Colors.white,
          borderRadius: BorderRadius.circular(10),
          border: Border.all(color: Colors.black, width: 1.5),
        ),
        child: Row(
          children: [
            Text(['ก.','ข.','ค.','ง.'][i], style: TextStyle(color: isSelected ? Colors.white : Colors.black, fontWeight: FontWeight.bold)),
            const SizedBox(width: 15),
            Expanded(child: Text(text, style: TextStyle(color: isSelected ? Colors.white : Colors.black))),
          ],
        ),
      ),
    );
  }
}

