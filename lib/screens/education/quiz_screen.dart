class Question {
  final String questionText;
  final List<String> options;
  final int correctAnswerIndex;
  int? userAnswerIndex;

  Question({required this.questionText, required this.options, required this.correctAnswerIndex});
}

class QuizSet {
  final String title;
  final List<Question> questions;
  QuizSet({required this.title, required this.questions});
}

class QuizCategory {
  final String titleEn;
  final String titleTh;
  final IconData icon;
  final Color color;
  final List<QuizSet> quizSets;
  QuizCategory({required this.titleEn, required this.titleTh, required this.icon, required this.color, required this.quizSets});
}

// ฟังก์ชันจัดกลุ่มข้อสอบ
List<QuizSet> _generateQuizzes(List<Question> pool) {
  int limit = pool.length;
  
  List<Question> getShuffled(int count) {
    if (limit == 0) return [];
    List<Question> copy = List<Question>.from(pool);
    copy.shuffle();
    return copy.sublist(0, min(count, copy.length));
  }

  return [
    QuizSet(title: 'Quiz 1', questions: limit > 0 ? pool.sublist(0, min(10, limit)) : []),
    QuizSet(title: 'Quiz 2', questions: limit > 10 ? pool.sublist(10, min(20, limit)) : []),
    QuizSet(title: 'Quiz 3', questions: limit > 20 ? pool.sublist(20, min(30, limit)) : []),
    QuizSet(title: 'Quiz 4', questions: getShuffled(10)),
    QuizSet(title: 'Quiz 5', questions: getShuffled(10)),
  ];
}

