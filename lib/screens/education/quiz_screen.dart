  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(backgroundColor: const Color(0xFF1D0A45), title: const Text('Quiz Summary', style: TextStyle(color: Color(0xFFFFD700))), automaticallyImplyLeading: false),
      body: Column(
        children: [
          Container(
            padding: const EdgeInsets.all(30),
            child: Column(
              children: [
                const Text('Total Score', style: TextStyle(fontSize: 20)),
                Text('$score/${quizSet.questions.length}', style: const TextStyle(fontSize: 60, fontWeight: FontWeight.bold)),
                const SizedBox(height: 10),
                const Text('เฉลยคำตอบ', style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: Colors.blueGrey)),
              ],
            ),
          ),
          Expanded(
            child: ListView.builder(
              padding: const EdgeInsets.symmetric(horizontal: 16),
              itemCount: quizSet.questions.length,
              itemBuilder: (context, index) {
